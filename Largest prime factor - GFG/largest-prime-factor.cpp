class Solution{
public: 
    long long int largestPrimeFactor(int n){
        // code here
        long long int ans=0;
        for(int i=2;i<=n;i++){
            while(n%i==0){
                ans=(long long int)i;
                n/=i;
            }
        }
        return ans;
    }
};

